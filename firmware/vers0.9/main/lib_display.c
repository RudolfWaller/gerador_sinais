void vDspLinha(int iX1, int iY1, int iX2, int iY2)
{
  int iTmp;
  int iX,iY;
  int iDx, iDy;
  int iErro;
  int iPassoY;

  bool bTrocaXY = FALSE;

  // ???
  /* no BBX intersection check at the moment, should be added... */
  // ???

  if(iX1==iX2){
    if(iY1>iY2){
      while(iY2<=iY1)
        vDspPonto(iX1,iY2++);
    }
    else{
      while(iY1<=iY2)
        vDspPonto(iX1,iY1++);
    }
    return;
  }
  else if(iY1==iY2){
    if(iX1>iX2){
      while(iX2<=iX1)
        vDspPonto(iX2++,iY1);
    }
    else{
      while(iX1<=iX2)
        vDspPonto(iX1++,iY1);
    }
    return;
  }

  if ( iX1 > iX2 )
    iDx = iX1-iX2;
  else
    iDx = iX2-iX1;

  if ( iY1 > iY2 )
    iDy = iY1-iY2;
  else
    iDy = iY2-iY1;

  if ( iDy > iDx ){
    bTrocaXY = 1;
    iTmp = iDx; iDx =iDy; iDy = iTmp;
    iTmp = iX1; iX1 =iY1; iY1 = iTmp;
    iTmp = iX2; iX2 =iY2; iY2 = iTmp;
  }

  if ( iX1 > iX2 ){
    iTmp = iX1; iX1 =iX2; iX2 = iTmp;
    iTmp = iY1; iY1 =iY2; iY2 = iTmp;
  }

  iErro = iDx >> 1;
  if ( iY2 > iY1 )
    iPassoY= 1;
  else
    iPassoY=-1;

  iY = iY1;
  for( iX = iX1; iX <= iX2; iX++ ){
    if ( !bTrocaXY)
      vDspPonto(iX, iY);
    else
      vDspPonto(iY, iX);
    iErro -= (char)iDy;
    if ( iErro < 0 ){
      iY += (int)iPassoY;
      iErro += (int)iDx;
    }
  }
}

