static void vDesenhaCirculo(int iX0, int iY0, int iRaio, uchar ucQuadrante)
{
    int iF;
    int iDdF_X;
    int iDdF_Y;
    int iIX;
    int iIY;

    iF = 1;
    iF -= iRaio;
    iDdF_X = 1;
    iDdF_Y = 0;
    iDdF_Y -= iRaio;
    iDdF_Y *= 2;
    iIX = 0;
    iIY = iRaio;

    vDesenhaSecaoCirculo(iIX, iIY, iX0, iY0, ucQuadrante);

    while ( iIX < iIY ){
      if (iF >= 0){
        iIY--;
        iDdF_Y += 2;
        iF += iDdF_Y;
      }
      iIX++;
      iDdF_X += 2;
      iF += iDdF_X;

      vDesenhaSecaoCirculo(iIX, iIY, iX0, iY0, ucQuadrante);
    }
}

