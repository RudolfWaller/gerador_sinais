void vDspPreenche(int iColuna, int iLinha)
{
  bool bOk;
  uchar ucMenorLinhaAtual,ucMaiorLinhaAtual;
  uchar vucMenorColunaAtual[PIXEL_Y],vucMaiorColunaAtual[PIXEL_Y];
  ulong uiConf=0,uiPonto=0;

  if(iColuna>=0 && iColuna<PIXEL_X &&
     iLinha >=0 && iLinha <PIXEL_Y &&
     !bDspLePonto(iColuna, iLinha)){
    memset(vuiPreencheDisplay,0,      sizeof(vuiPreencheDisplay));
    memset(vucMenorColuna,    iColuna,sizeof(vucMenorColuna));
    memset(vucMaiorColuna,    iColuna,sizeof(vucMaiorColuna));

    ucMenorLinha=iLinha;
    ucMaiorLinha=iLinha;
    vAdicionaPonto(iColuna, iLinha);

    bOk=FALSE;
    while(!bOk){
      bOk=TRUE;
      ucMenorLinhaAtual=ucMenorLinha;
      ucMaiorLinhaAtual=ucMaiorLinha;
      ucMaiorLinha=ucMenorLinhaAtual;
      ucMenorLinha=ucMaiorLinhaAtual;

      memcpy(vucMenorColunaAtual,vucMenorColuna,sizeof(vucMenorColunaAtual));
      memcpy(vucMaiorColunaAtual,vucMaiorColuna,sizeof(vucMaiorColunaAtual));
      memset(vucMenorColuna,128,sizeof(vucMenorColuna));
      memset(vucMaiorColuna,  0,sizeof(vucMaiorColuna));

      for(iLinha=ucMenorLinhaAtual;iLinha<=ucMaiorLinhaAtual;iLinha++){
        for(iColuna=vucMenorColunaAtual[iLinha];iColuna<=vucMaiorColunaAtual[iLinha];iColuna++){
          uiConf++;
          if(bDspLePonto2(vuiPreencheDisplay, iColuna, iLinha)){
            uiPonto++;
            vDspPonto(iColuna, iLinha);
            vAdicionaPonto(iColuna,iLinha-1);
            vAdicionaPonto(iColuna,iLinha+1);
            vAdicionaPonto(iColuna-1,iLinha);
            vAdicionaPonto(iColuna+1,iLinha);

            bOk=FALSE;

            vDspBranco2(vuiPreencheDisplay,iColuna, iLinha);
          }
        }
      }
    }
  }
}

