void vDspAscii(char cCh)
{
  const char *pccAscii=NULL;
  char cL,cC,cB,cColuna;

  const int *pciAscii=NULL;
  uint uiB,uiColuna,uiInvAscii;

  if(ucAlturaAscii>8){
    if(ucInvAscii)
      uiInvAscii=0xffff;
    else
      uiInvAscii=0x0000;

    if(cCh>' '){
      pciAscii=(const int *)cvsFontes[ucFonteAtual].pccFonte;
      pciAscii+=ucLarguraAscii*(cCh-'!');
    }

    for(cC=0;cC<ucLarguraAscii;cC++){
      if(cCh>' ')
        uiColuna=*pciAscii++;
      else
        uiColuna=0x0000;

      uiB=(uint) (1<<(ucAlturaAscii-1));
      for(cL=0;cL<ucAlturaAscii;cL++){
        if((uiColuna ^ uiInvAscii) & uiB)
          vDspPonto(cColAscii+cC,cLinAscii+cL);
        else
          vDspBranco(cColAscii+cC,cLinAscii+cL);
        uiB>>=1;

      }
    }
  }
  else{
    if(cCh>' '){
      pccAscii=cvsFontes[ucFonteAtual].pccFonte;
      pccAscii+=ucLarguraAscii*(cCh-'!');
    }

    for(cC=0;cC<ucLarguraAscii;cC++){
      if(cCh>' ')
        cColuna=*pccAscii++;
      else
        cColuna=0x0000;

      cB=(char) (1<<(ucAlturaAscii-1));
      for(cL=0;cL<ucAlturaAscii;cL++){
        if((cColuna ^ ucInvAscii) & cB)
          vDspPonto(cColAscii+cC,cLinAscii+cL);
        else
          vDspBranco(cColAscii+cC,cLinAscii+cL);
        cB>>=1;
      }
    }
  }
  cColAscii+=ucLarguraAscii+1;

  if(ucInvAscii){
    vDspLinha(cColAscii-1,cLinAscii+ucAlturaAscii-1,cColAscii-1,cLinAscii);
  }

  if(cColAscii>PIXEL_X-ucLarguraAscii){
    cColAscii=0;
    cLinAscii-=ucAlturaAscii+1;
  }
}

