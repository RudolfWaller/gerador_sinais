uchar ucMenorLinha, ucMaiorLinha;
uchar vucMenorColuna[PIXEL_Y],vucMaiorColuna[PIXEL_Y];

static void vAdicionaPonto(int iColuna, int iLinha)
{
  if(iColuna>=0 && iColuna<PIXEL_X &&
     iLinha >=0 && iLinha <PIXEL_Y &&
     !bDspLePonto2(vuiPreencheDisplay, iColuna, iLinha) &&
     !bDspLePonto(iColuna, iLinha)){
    vDspPonto2(vuiPreencheDisplay,iColuna, iLinha);
    if(iLinha<ucMenorLinha)
      ucMenorLinha=iLinha;
    if(iLinha>ucMaiorLinha)
      ucMaiorLinha=iLinha;

    if(iColuna<vucMenorColuna[iLinha])
      vucMenorColuna[iLinha]=iColuna;
    if(iColuna>vucMaiorColuna[iLinha])
      vucMaiorColuna[iLinha]=iColuna;
  }
}

