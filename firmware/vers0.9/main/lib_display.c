void vDspPonto2(tTipoDisplay vuiDisplay, int iCol, int iLin)
{
  if(iLin>=0 && iLin<PIXEL_Y && iCol>=0 && iCol<PIXEL_X){
    uint uiEndereco;

    uiEndereco=PIXEL_X*((PIXEL_Y-1)-iLin)+iCol;
    vuiDisplay[uiEndereco/16] |= cvsCamadaMascara[ucMascaraAtual][uiEndereco/16] & cvuiMascara[uiEndereco % 16];

    bDisplayAlterado=TRUE;
  }
}

