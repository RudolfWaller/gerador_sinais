void vDspMostraNum(uint uiNum,int iNumCaracteres)
{
  char  vcString[10];
  uchar ucPos;

  if(uiNum==0){
    vcString[0]='0';
    ucPos=1;
  }
  else{
    for(ucPos=0;uiNum!=0;ucPos++){
      vcString[ucPos]=(char)('0'+uiNum % 10);
      uiNum/=10;
    }
  }

  if(iNumCaracteres>0)
    while(ucPos<iNumCaracteres)
      vcString[ucPos++]=' ';
  else{
    iNumCaracteres=0-iNumCaracteres;
    while(ucPos<iNumCaracteres)
      vcString[ucPos++]='0';
  }

  vcString[ucPos]=0;

  //------------------------

  ucPos--;
  while(iNumCaracteres--)
    vDspAscii(vcString[ucPos--]);
}

